package com.withelper.adapter;

public class HotBookInfo {
	
	private String name;          
    private String author;        
    private String press;
    private String callno;
    private String collections;
    private String lend_count;
    private String lend_ratio;

	public HotBookInfo() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getPress() {
		return press;
	}

	public void setPress(String press) {
		this.press = press;
	}

	public String getCallno() {
		return callno;
	}

	public void setCallno(String callno) {
		this.callno = callno;
	}

	public String getCollections() {
		return collections;
	}

	public void setCollections(String collections) {
		this.collections = collections;
	}

	public String getLend_count() {
		return lend_count;
	}

	public void setLend_count(String lend_count) {
		this.lend_count = lend_count;
	}

	public String getLend_ratio() {
		return lend_ratio;
	}

	public void setLend_ratio(String lend_ratio) {
		this.lend_ratio = lend_ratio;
	}
	

}
